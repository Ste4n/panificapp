                2. Información que Recopilamos
                a. Información Personal
                Podemos recopilar la siguiente información personal cuando usted se registra o utiliza PanificApp:

                Nombre
                Dirección de correo electrónico
                Número de teléfono
                Información de la empresa (nombre de la empresa, dirección, etc.)
                Información de pago (en caso de realizar transacciones a través de la aplicación)
                b. Información de Uso
                Recopilamos información sobre cómo accede y utiliza nuestra aplicación, incluyendo:

                Dirección IP
                Tipo de dispositivo
                Navegador y sistema operativo
                Páginas visitadas y acciones realizadas en la aplicación
                Tiempos de acceso y fechas
                c. Cookies y Tecnologías Similares
                Utilizamos cookies y tecnologías similares para recopilar información sobre su uso de la aplicación y
                mejorar su experiencia.

                3. Uso de la Información
                Utilizamos la información que recopilamos para los siguientes fines:

                Proporcionar y mantener nuestra aplicación
                Procesar y gestionar sus pedidos y transacciones
                Comunicarnos con usted, incluyendo responder a sus consultas y proporcionarle actualizaciones sobre
                nuestra aplicación
                Mejorar y personalizar su experiencia en la aplicación
                Analizar el uso de la aplicación para mejorar nuestros servicios
                Cumplir con las obligaciones legales y proteger nuestros derechos
                4. Compartir su Información
                No vendemos, alquilamos ni compartimos su información personal con terceros, excepto en las siguientes
                circunstancias:

                Con proveedores de servicios de confianza que nos ayudan a operar nuestra aplicación y realizar nuestras
                actividades comerciales
                Para cumplir con las leyes aplicables, regulaciones, procesos legales o solicitudes gubernamentales
                Para proteger nuestros derechos, propiedad o seguridad, así como los de nuestros usuarios y el público
